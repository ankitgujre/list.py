lst = ["banana", "litchi", "apple"]

lst.append("4")  # add element at the end of list
print(lst)

lst.sort()
print(lst)  # sort in ascending order by default

lst.sort(reverse=True)
print(lst)

lst.reverse()
print(lst)

lst.insert(1, 5)
print(lst)


